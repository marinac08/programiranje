#include <iostream>
#include <vector>

void insertionSort(std::vector<int>& arr) {
    int n = arr.size();
    for (int i = 1; i < n; i++) {
        int key = arr[i];
        int j = i - 1;

        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }

        arr[j + 1] = key;
    }
}

void printList(const std::vector<int>& arr) {
    for (int num : arr)
        std::cout << num << " ";
    std::cout << std::endl;
}

int main() {
    std::vector<int> numbers = {5, 2, 9, 1, 7};

    std::cout << "Original list: ";
    printList(numbers);

    insertionSort(numbers);

    std::cout << "Sorted list (Insertion Sort): ";
    printList(numbers);

    return 0;
}
